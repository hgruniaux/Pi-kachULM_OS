#ifndef __PIKAOS_LIBC_SYS_SYSCALL_TABLE_H__
#define __PIKAOS_LIBC_SYS_SYSCALL_TABLE_H__

enum {
  /* Process system calls. */
  SYS_EXIT,
  SYS_PRINT,
  SYS_GETPID,
  SYS_DEBUG,
  SYS_SPAWN,

  /* Scheduler system calls. */
  SYS_SLEEP,
  SYS_YIELD,
  SYS_SCHED_SET_PRIORITY,
  SYS_SCHED_GET_PRIORITY,

  /* Pipe system calls. */
  SYS_PIPE_OPEN,
  SYS_PIPE_CLOSE,
  SYS_PIPE_GET,
  SYS_PIPE_READ,
  SYS_PIPE_WRITE,

  /* Memory and heap segment system calls. */
  SYS_SBRK,

  /* Framebuffer system calls. */
  SYS_GET_FRAMEBUFFER,

  /* Filesystem system calls. */
  SYS_OPEN_FILE,
  SYS_CLOSE_FILE,
  SYS_READ_FILE,
  SYS_GET_FILE_SIZE,

  SYS_OPEN_DIR,
  SYS_CLOSE_DIR,
  SYS_READ_DIR,

  /* Window manager system calls. */
  SYS_POLL_MESSAGE,
  SYS_WAIT_MESSAGE,
  SYS_WINDOW_CREATE,
  SYS_WINDOW_DESTROY,
  SYS_WINDOW_SET_TITLE,
  SYS_WINDOW_GET_VISIBILITY,
  SYS_WINDOW_SET_VISIBILITY,
  SYS_WINDOW_GET_GEOMETRY,
  SYS_WINDOW_SET_GEOMETRY,
  SYS_WINDOW_PRESENT,

  /* Window graphics system calls. */
  SYS_GFX_CLEAR,
  SYS_GFX_DRAW_LINE,
  SYS_GFX_DRAW_RECT,
  SYS_GFX_FILL_RECT,
  SYS_GFX_DRAW_TEXT,
  SYS_GFX_BLIT
};

#endif  // !__PIKAOS_LIBC_SYS_SYSCALL_TABLE_H__
